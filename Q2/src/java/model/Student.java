package model;

/**
 *
 * @author dell
 */
public class Student {
    private String name;
    private boolean gender;
    private String color;

    public Student() {
    }

    public Student(String name, boolean gender, String color) {
        this.name = name;
        this.gender = gender;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

 
    
    
}
